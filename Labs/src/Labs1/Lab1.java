package Labs1;

public class Lab1 {
    public static void main(String[] args) {
        boolean boo = true;
        byte maxByte = 127;
        short minShort = -32_768;
        int maxInt = 2_147_483_647;
        long javaFlex = 3_000_000_000L;
        float pie = 3.14159f;
        double doubleYum = 3.14159 * 2;
        
        System.out.println("Boolean:" + boo);
        System.out.println("Byte:" + maxByte);
        System.out.println("Short: " + minShort);
        System.out.println("Int:" + maxInt);
        System.out.println("Long:" + javaFlex);
        System.out.println("Float:" + pie);
        System.out.println("Double:" + doubleYum);
    }
}

